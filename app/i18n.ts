const messages = {
  en,
  "zh-CN": zh
} satisfies Record<Locale, Record<keyof typeof en, string>>;

export type MessageKey = keyof typeof en;

export function normalizeLocale(value?: string | null): Locale {
  return value === "zh-CN" ? "zh-CN" : "en";
}

export function t(locale: Locale, key: MessageKey, values?: Record<string, string | number>) {
  const template = messages[locale][key] ?? messages.en[key] ?? key;
  if (!values) return template;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(values[name] ?? `{${name}}`));
}

export function translateStatus(locale: Locale, value?: string | null) {
  const key = String(value ?? "").replace(/-/g, "_");
  const map: Record<string, MessageKey> = {
    active: "active",
    draft: "draft",
    blocked: "blocked",
    ready: "ready",
    queued: "queued",
    sent: "sent",
    failed: "failed",
    sending: "sending",
    approved: "approved",
    completed: "completed",
    suppressed: "suppressed",
    unsubscribed: "unsubscribed",
    bounced: "bounced",
    completed_with_failures: "completedWithFailures",
    empty: "empty",
    needs_review: "needsReview",
    needs_reply: "needsReply",
    resolved: "resolved",
    normal: "normal"
  };
  return map[key] ? t(locale, map[key]) : value ?? "";
}
